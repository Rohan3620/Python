print("Line1\nLine2")              # New line
print("Tab\tSpace")               # Tab space
print("Backslash: \\")            # Prints \
print('It\'s Rohan\'s book')      # Single quote inside string
print("He said \"Hello\"")        # Double quotes inside
print("Hello\rHi")                # Carriage return (overwrites)
print("Helloo\b")                 # Backspace (removes 'o')
print("Bell sound\a")             # Bell (may beep)
print("Octal:\041")               # Octal for '!'
print("Hex:\x21")                 # Hex for '!'
