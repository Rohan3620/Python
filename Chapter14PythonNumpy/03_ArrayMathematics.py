import numpy as np
x = np.array([1, 2, 3])
y = np.array([4, 5, 6])

print("\nAddition:", x + y)
print("Multiplication:", x * y)
print("Square:", x**2)
print("Sum:", np.sum([x,y]))
print("subtraction:", np.subtract(x,y))
print("multiply:", np.multiply([x,y]))
print("Sum:", np.sum([x,y]))

print("Mean:", np.mean(x))
print("Max:", np.max(y))
print("Min:", np.min(y))
print("Median:", np.median(y))
