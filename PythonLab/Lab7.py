import math

n = int(input("Enter a number: "))

print(f"Factorial of {n} is {math.factorial(n)}")
print(f"5 to the power of 2 is {math.pow(5, 2)}") 
print(f"Square root of {n} is {math.sqrt(n)}")
print(f"Floor of sqrt({n}) is {math.floor(math.sqrt(n))}")
print(f"Ceil of sqrt({n}) is {math.ceil(math.sqrt(n))}")
print(f"Value of sin(90°) is {math.sin(math.radians(90))}")
print(f"Value of cos(90°) is {math.cos(math.radians(90))}")
print(f"Value of tan(90°) is {math.tan(math.radians(90))}")
print(f"{math.pi} radians in degrees is {math.degrees(math.pi)}")
print(f"180 degrees in radians is {math.radians(180)}")
