# In Python, membership operators are used to test whether a value or variable is a member of a sequence (like string, list, tuple, set, dictionary)

print("String") 
a = "Rohan"
print("'R' in string:", 'R' in a)
print("'R' not in string:", 'R' not in a)

print("List")
a = ['R', 'o', 'h', 'a', 'n']
print("'R' in list:", 'R' in a)
print("'R' not in list:", 'R' not in a)

print("Tuple") 
a = ('R', 'o', 'h', 'a', 'n')
print("'R' in tuple:", 'R' in a)
print("'R' not in tuple:", 'R' not in a)

print("Set") 
a = {'R', 'o', 'h', 'a', 'n'}
print("'R' in set:", 'R' in a)
print("'R' not in set:", 'R' not in a)

print(" Dictionary")
a = {"name": "Ravi", "age": 20}
print("'name' in dictionary keys:", 'name' in a)
print("'name' not in dictionary keys:", 'name' not in a)
